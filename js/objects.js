'use strict';
function Dog(dogname, dogbreed, dogage) {
  this.name = dogname;
  this.age = dogage;
  this.breed = dogbreed;
}

function Cat(catname, catbreed,catage ) {
  this.name = catname;
  this.age = catage;
  this.breed = catbreed;

}

function Ferret(ferretName, ferretAge, ferretFood) {
  this.name = ferretName;
  this.age = ferretAge;
  this.foodRequirements = ferretFood;

}

function Bird(birdName, birdBreed) {
  this.name = birdName;
  this.breed = birdBreed;

}

function Fish(fishName, fishSpecies, fishWaterCondition) {
  this.name = fishName;
  this.species = fishSpecies;
  this.waterConditions = fishWaterCondition;

}

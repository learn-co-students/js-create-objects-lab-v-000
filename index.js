// 'use strict';
function Dog(name, breed, age) {
  this.name = name
  this.breed = breed
  this.age = age
}
function Cat(name, breed, age) {
  this.name = name
  this.breed = breed
  this.age = age
}
function Ferret(name, age, foods) {
  this.name = name
  this.age = age
  this.foodRequirements = foods
}
function Bird(name, breed) {
  this.name = name
  this.breed = breed
}
function Fish(name, species, waterComponents) {
  this.name = name
  this.species = species
  this.waterConditions = waterComponents
}

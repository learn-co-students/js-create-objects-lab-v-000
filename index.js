'use strict';

function Dog(name, breed, age) {
  this.name = name; 
  this.breed = breed;
  this.age = age;
}

function Cat(name, breed, age) {
  this.name = name; 
  this.breed = breed;
  this.age = age;
}

function Ferret(name, age, fr) {
  this.name = name;
  this.age = age;
  this.foodRequirements = fr;
}

function Bird(name, breed) {
  this.name = name; 
  this.breed = breed;
}

function Fish(name, species, wc) {
  this.name = name;
  this.species = species;
  this.waterConditions = wc;
}

